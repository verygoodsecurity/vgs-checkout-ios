import Foundation

/// HTTP request methods.
public enum VGSCheckoutHTTPMethod: String {
		/// GET method.
		case get     = "GET"
		/// POST method.
		case post    = "POST"
		/// PUT method.
		case put     = "PUT"
		/// PATCH method.
		case patch = "PATCH"
		/// DELETE method.
		case delete = "DELETE"
}
